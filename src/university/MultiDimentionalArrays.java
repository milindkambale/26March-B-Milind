package university;

public class MultiDimentionalArrays {

	public static void main(String[] args) 
	{
		String name[][]= {{"Velocity","Corporate","Training"},{"Centre","Katraj","Pune"}};
		for(int i=0;i<=1;i++)
		{
			for(int j=0;j<3;j++)
			{
				System.out.print(name[i][j]+" ");
			}
			System.out.println();
	
	

}
	}
}
